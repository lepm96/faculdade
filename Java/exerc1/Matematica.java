package exerc1;

public class Matematica {

    int a;
    int b;

    public int adicao(int a, int b) {
        return a + b;
    }

    public int subtracao(int a, int b) {
        return a - b;
    }

    public int divisao(int a, int b) {
        return a / b;
    }

    public int multiplicacao(int a, int b) {
        return a * b;
    }
}
